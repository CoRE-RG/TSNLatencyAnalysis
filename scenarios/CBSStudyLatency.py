class CBSMaxLatency(NetworkLatencyAnalysis):

    def __init__(
        self,
        numStages,
        numInputLinks,
        aggregateIdleSlope,
        stageIdleSlope=[],
        includeCrossFlows=False,
        calculateLinkIdleSlopes=False,
        keepMaxFrameSize=False,
    ):
        super().__init__(linkspeed=LINK_SPEED, cmi=CMI, switch_delay=SWITCH_DELAY)
        self.numStages = numStages
        self.numInputLinks = numInputLinks
        self.aggregateIdleSlope = aggregateIdleSlope
        self.totalStreamBandwidth = 75000000
        if len(stageIdleSlope) == 0:
            self.stageIdleSlope = [aggregateIdleSlope / numInputLinks]
            self.stageIdleSlopeSet = False
        else:
            self.stageIdleSlope = stageIdleSlope
            self.stageIdleSlopeSet = True
        if len(stageIdleSlope) == 1:
            self.stageIdleSlope = stageIdleSlope * numStages
        self.includeCrossFlows = includeCrossFlows
        self.keepMaxFrameSize = keepMaxFrameSize
        self.network.addNode("listener")
        self.network.addNode("beSrc")
        self.network.addBridge("aggregateSwitch")
        for j in range(numInputLinks):
            self.network.addNode("talker" + str(j))
            for i in range(numStages):
                self.network.addNode("beSrcIn" + str(j) + "S" + str(i))
                self.network.addBridge("stageSwitchIn" + str(j) + "S" + str(i))
        self.createLinks()
        self.addFlowsAndPaths()
        self.calculateLinkIdleSlopes = calculateLinkIdleSlopes
        if calculateLinkIdleSlopes:
            self.network.calculateLinkIdleSlopesFromFlows(CMI)

    def createLinks(self):
        """
        Helper function to set up the small network for evaluation.
        """
        LINK_SPEED = 100000000
        self.network.addBidirectionalLink(
            "aggregateSwitch",
            "listener",
            LINK_SPEED,
            SWITCH_DELAY,
            self.aggregateIdleSlope,
        )
        self.network.addBidirectionalLink("beSrc", "aggregateSwitch", LINK_SPEED, SWITCH_DELAY, 0)
        for j in range(self.numInputLinks):
            self.network.addBidirectionalLink(
                "talker" + str(j),
                "stageSwitchIn" + str(j) + "S0",
                LINK_SPEED,
                SWITCH_DELAY,
                ceil(self.totalStreamBandwidth / self.numInputLinks),
            )
            for i in range(self.numStages):
                self.network.addBidirectionalLink(
                    "beSrcIn" + str(j) + "S" + str(i),
                    "stageSwitchIn" + str(j) + "S" + str(i),
                    LINK_SPEED,
                    SWITCH_DELAY,
                    0,
                )
                if i < self.numStages - 1:
                    self.network.addBidirectionalLink(
                        "stageSwitchIn" + str(j) + "S" + str(i),
                        "stageSwitchIn" + str(j) + "S" + str(i + 1),
                        LINK_SPEED,
                        SWITCH_DELAY,
                        self.stageIdleSlope[i],
                    )
            self.network.addBidirectionalLink(
                "stageSwitchIn" + str(j) + "S" + str(self.numStages - 1),
                "aggregateSwitch",
                LINK_SPEED,
                SWITCH_DELAY,
                self.stageIdleSlope[i],
            )

    def addFlowsAndPaths(self):
        """
        Helper function to add flows and paths to the small network for evaluation.
        """
        self.talkerPacketSize = floor(((self.totalStreamBandwidth / 8) * CMI) / self.numInputLinks) - IFG / 8
        for j in range(self.numInputLinks):
            flow = Flow("talker" + str(j), "listener", self.talkerPacketSize, 0.01, CMI, 7)
            path = Path("talker" + str(j), "listener", [])
            path.addLinks(self.network.getLink("talker" + str(j), "stageSwitchIn" + str(j) + "S0"))
            for i in range(self.numStages - 1):
                path.addLinks(
                    self.network.getLink(
                        "stageSwitchIn" + str(j) + "S" + str(i),
                        "stageSwitchIn" + str(j) + "S" + str(i + 1),
                    )
                )
            path.addLinks(
                self.network.getLink(
                    "stageSwitchIn" + str(j) + "S" + str(self.numStages - 1),
                    "aggregateSwitch",
                )
            )
            path.addLinks(self.network.getLink("aggregateSwitch", "listener"))
            self.network.addFlow(flow)
            self.network.addPath(path)
        if self.includeCrossFlows:
            self.addCrossFlows(self.keepMaxFrameSize)

    def addCrossFlows(self, keepMaxFrameSize=False):
        if self.numInputLinks == 1:
            return
        crossFlowPacketSize = (
            floor(((self.totalStreamBandwidth - self.totalStreamBandwidth / self.numInputLinks) / 8) * CMI) - IFG / 8
        )
        crossFlowPeriod = 0.01
        if keepMaxFrameSize:
            crossFlowPacketSize = self.talkerPacketSize
        flowMultiplier = 1
        if keepMaxFrameSize:
            flowMultiplier = self.numInputLinks - 1
        for k in range(flowMultiplier):
            for j in range(self.numInputLinks - 1):
                crossFlow = Flow(
                    "beSrcIn" + str(j) + "S" + str(self.numStages - 1),
                    "beSrcIn" + str(j + 1) + "S" + str(self.numStages - 1),
                    crossFlowPacketSize,
                    1.0,
                    crossFlowPeriod,
                    7,
                )
                crossPath = Path(
                    "beSrcIn" + str(j) + "S" + str(self.numStages - 1),
                    "beSrcIn" + str(j + 1) + "S" + str(self.numStages - 1),
                    [],
                )
                crossPath.addLinks(
                    self.network.getLink(
                        "beSrcIn" + str(j) + "S" + str(self.numStages - 1),
                        "stageSwitchIn" + str(j) + "S" + str(self.numStages - 1),
                    )
                )
                crossPath.addLinks(
                    self.network.getLink(
                        "stageSwitchIn" + str(j) + "S" + str(self.numStages - 1),
                        "aggregateSwitch",
                    )
                )
                crossPath.addLinks(
                    self.network.getLink(
                        "aggregateSwitch",
                        "stageSwitchIn" + str(j + 1) + "S" + str(self.numStages - 1),
                    )
                )
                crossPath.addLinks(
                    self.network.getLink(
                        "stageSwitchIn" + str(j + 1) + "S" + str(self.numStages - 1),
                        "beSrcIn" + str(j + 1) + "S" + str(self.numStages - 1),
                    )
                )
                self.network.addFlow(crossFlow)
                self.network.addPath(crossPath)
            crossFlow = Flow(
                "beSrcIn" + str(self.numInputLinks - 1) + "S" + str(self.numStages - 1),
                "beSrcIn" + str(0) + "S" + str(self.numStages - 1),
                crossFlowPacketSize,
                1.0,
                crossFlowPeriod,
                7,
            )
            crossPath = Path(
                "beSrcIn" + str(self.numInputLinks - 1) + "S" + str(self.numStages - 1),
                "beSrcIn" + str(0) + "S" + str(self.numStages - 1),
                [],
            )
            crossPath.addLinks(
                self.network.getLink(
                    "beSrcIn" + str(self.numInputLinks - 1) + "S" + str(self.numStages - 1),
                    "stageSwitchIn" + str(self.numInputLinks - 1) + "S" + str(self.numStages - 1),
                )
            )
            crossPath.addLinks(
                self.network.getLink(
                    "stageSwitchIn" + str(self.numInputLinks - 1) + "S" + str(self.numStages - 1),
                    "aggregateSwitch",
                )
            )
            crossPath.addLinks(
                self.network.getLink(
                    "aggregateSwitch",
                    "stageSwitchIn" + str(0) + "S" + str(self.numStages - 1),
                )
            )
            crossPath.addLinks(
                self.network.getLink(
                    "stageSwitchIn" + str(0) + "S" + str(self.numStages - 1),
                    "beSrcIn" + str(0) + "S" + str(self.numStages - 1),
                )
            )
            self.network.addFlow(crossFlow)
            self.network.addPath(crossPath)

    def runStudy(self, filename):
        flow = self.network.getFlow("talker0", "listener")
        self.evaluateDelayForFlow(filename, flow, False)
        self.evaluateDelayForFlow(filename, flow, True)

    def evaluateDelayForFlow(self, filename, flow, calc_end_to_end=False):
        result = dict()
        if not calc_end_to_end:
            link = self.network.getLink("aggregateSwitch", "listener")
            queue = "AggregateSwitch-Listener"
            classMaxFrame = self.network.findClassMaxFrame() * 8 + IFG
            result[flow] = self.calculateQueueDelayForLink(link, flow, classMaxFrame)
        else:
            result[flow] = self.calculateEndToEndDelayForFlow(flow)
            queue = "EndToEnd"
        resultCols = [
            "Inputs",
            "Stages",
            "SwitchQueue",
            "qStandardL3V1",
            "qStandardL3V2",
            "qStandardL3V3",
        ]
        additionalValues = {
            "Inputs": str(self.numInputLinks),
            "Stages": str(self.numStages),
            "SwitchQueue": queue,
        }
        self.writeToCsv(filename, result, resultCols, additionalValues)


